export default function projectFactory(name) {
  let toDoList = [];

  const getToDos = () => {
    return toDoList;
  };
  const addToDo = (newToDo) => {
    toDoList.splice(0, 0, newToDo);
  };
  const deleteToDo = (indexOfToDo) => {
    toDoList.splice(indexOfToDo, 1);
  };
  const getName = () => name;
  return { getName, getToDos, addToDo, deleteToDo };
}
